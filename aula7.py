# Variáveis são usadas para salvar algo
# na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, 
# pode usar números e underline _.
# O sinal de = é o operador de atribuição. Ele é
# usado para atribuir um valor e um nome (variável).
# Uso: nome_variavel = expressão
nome_completo = 'Esdras Trindade de Lima Raiz'
print(nome_completo) # Esdras Trindade de Lima Raiz
soma_dois_mais_dois = 2 + 2
print(soma_dois_mais_dois)
# observa que a gente atribui um "apelido" ao valor
# Variáveis não são utilizados para abreviar
# código, são usados para fazer código.
